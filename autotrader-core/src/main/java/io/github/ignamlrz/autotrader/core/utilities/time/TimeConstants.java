package io.github.ignamlrz.autotrader.core.utilities.time;

/**
 * Constants for use on package Time
 */
final class TimeConstants {
    /**
     * Minute to seconds
     */
    public static long MINUTE_TO_SECONDS = 60L;

    /**
     * Hour to seconds
     */
    public static long HOUR_TO_SECONDS = 60L * MINUTE_TO_SECONDS;

    /**
     * Day to seconds
     */
    public static long DAY_TO_SECONDS = 24L * HOUR_TO_SECONDS;

    /**
     * Week to seconds
     */
    public static long WEEK_TO_SECONDS = 7L * DAY_TO_SECONDS;
}
